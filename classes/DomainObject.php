<?php

namespace classes\domain;


class DomainObject {
    private $id;

    function __construct( $id = null ) {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

} 